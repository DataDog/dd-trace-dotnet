        ciOutput.Should().NotBeNullOrEmpty();
        appSecOutput.Should().NotBeNullOrEmpty();
        tracerOutput.Should().NotBeNullOrEmpty();

        ciOutput.Should().Contain(expectedCIOutput);
        appSecOutput.Should().Contain(expectedAppSecOutput);
