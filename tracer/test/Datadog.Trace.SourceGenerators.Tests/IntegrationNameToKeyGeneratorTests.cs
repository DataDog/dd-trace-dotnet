using System.Linq;
using FluentAssertions;
using FluentAssertions.Execution;
using Xunit;

namespace Datadog.Trace.SourceGenerators.Tests;

public class IntegrationNameToKeyGeneratorTests
{
    [Fact]
    public void GeneratesIntegrationNameToKeysSwitch()
    {
        const string sourceCode = """
            using System;

            namespace Datadog.Trace.ClrProfiler.AutoInstrumentation.TestIntegration
            {
                [InstrumentMethod(
                    AssemblyName = "TestAssembly",
                    TypeName = "TestType",
                    MethodName = "TestMethod",
                    ReturnTypeName = "System.Void",
                    MinimumVersion = "1.0.0",
                    MaximumVersion = "2.0.0",
                    IntegrationName = "TestIntegration")]
                public static class TestIntegration
                {
                }

                [InstrumentMethod(
                    AssemblyName = "AnotherAssembly",
                    TypeName = "AnotherType",
                    MethodName = "AnotherMethod",
                    ReturnTypeName = "System.Void",
                    MinimumVersion = "1.0.0",
                    MaximumVersion = "2.0.0",
                    IntegrationName = "GraphQL")]
                public static class GraphQLIntegration
                {
                }
            }

            [AttributeUsage(AttributeTargets.Class | AttributeTargets.Struct, AllowMultiple = true)]
            public class InstrumentMethodAttribute : Attribute
            {
                public string AssemblyName { get; set; }
                public string TypeName { get; set; }
                public string MethodName { get; set; }
                public string ReturnTypeName { get; set; }
                public string MinimumVersion { get; set; }
                public string MaximumVersion { get; set; }
                public string IntegrationName { get; set; }
            }
            """;

        const string expectedOutput = """
            // <auto-generated/>
            #nullable enable

            namespace Datadog.Trace.Configuration
            {
                /// <summary>
                /// Generated mapping of integration names to their configuration keys.
                /// </summary>
                internal static partial class IntegrationNameToKeys
                {
                    /// <summary>
                    /// Gets the configuration keys for the specified integration name.
                    /// Returns keys in order: [canonical key, alias1, alias2]
                    /// </summary>
                    /// <param name="integrationName">The integration name</param>
                    /// <returns>Array of configuration keys to check in order</returns>
                    public static string[] GetIntegrationEnabledKeys(string integrationName)
                    {
                        return integrationName switch
                        {
                            "GraphQL" => new[] { "DD_TRACE_GRAPHQL_ENABLED", "DD_TRACE_GraphQL_ENABLED", "DD_GraphQL_ENABLED" },
                            "TestIntegration" => new[] { "DD_TRACE_TESTINTEGRATION_ENABLED", "DD_TRACE_TestIntegration_ENABLED", "DD_TestIntegration_ENABLED" },
                            _ => new[]
                            {
                                string.Format("DD_TRACE_{0}_ENABLED", integrationName.ToUpperInvariant()),
                                string.Format("DD_TRACE_{0}_ENABLED", integrationName),
                                $"DD_{integrationName}_ENABLED"
                            }
                        };
                    }
                }
            }
            """;

        var (diagnostics, outputs) = TestHelpers.GetGeneratedTrees<IntegrationNameToKeyGenerator>(
            [sourceCode],
            assertOutput: false);
        var output = outputs.FirstOrDefault() ?? string.Empty;

        using var s = new AssertionScope();
        diagnostics.Should().BeEmpty();

        AssertContains(output, expectedOutput);
    }

    [Fact]
    public void HandlesDuplicateIntegrationNames()
    {
        const string sourceCode = """
            using System;

            namespace Datadog.Trace.ClrProfiler.AutoInstrumentation.TestIntegration
            {
                [InstrumentMethod(
                    AssemblyName = "TestAssembly",
                    TypeName = "TestType",
                    MethodName = "TestMethod",
                    ReturnTypeName = "System.Void",
                    MinimumVersion = "1.0.0",
                    MaximumVersion = "2.0.0",
                    IntegrationName = "TestIntegration")]
                public static class TestIntegration1
                {
                }

                [InstrumentMethod(
                    AssemblyName = "TestAssembly",
                    TypeName = "TestType2",
                    MethodName = "TestMethod2",
                    ReturnTypeName = "System.Void",
                    MinimumVersion = "1.0.0",
                    MaximumVersion = "2.0.0",
                    IntegrationName = "TestIntegration")]
                public static class TestIntegration2
                {
                }
            }

            [AttributeUsage(AttributeTargets.Class | AttributeTargets.Struct, AllowMultiple = true)]
            public class InstrumentMethodAttribute : Attribute
            {
                public string AssemblyName { get; set; }
                public string TypeName { get; set; }
                public string MethodName { get; set; }
                public string ReturnTypeName { get; set; }
                public string MinimumVersion { get; set; }
                public string MaximumVersion { get; set; }
                public string IntegrationName { get; set; }
            }
            """;

        const string expectedOutput = """
            "TestIntegration" => new[] { "DD_TRACE_TESTINTEGRATION_ENABLED", "DD_TRACE_TestIntegration_ENABLED", "DD_TestIntegration_ENABLED" },
            """;

        var (diagnostics, outputs) = TestHelpers.GetGeneratedTrees<IntegrationNameToKeyGenerator>(
            [sourceCode],
            assertOutput: false);
        var output = outputs.FirstOrDefault() ?? string.Empty;

        using var s = new AssertionScope();
        diagnostics.Should().BeEmpty();

        // Should only have one entry for TestIntegration despite duplicate attributes
        output.Split('\n').Count(line => line.Contains("TestIntegration")).Should().Be(1);
        AssertContains(output, expectedOutput);
    }

