    [Fact]
    public void GeneratesCorrectKeyFormats()
    {
        const string sourceCode = """
            using System;

            namespace Datadog.Trace.ClrProfiler.AutoInstrumentation.TestIntegration
            {
                [InstrumentMethod(
                    AssemblyName = "TestAssembly",
                    TypeName = "TestType",
                    MethodName = "TestMethod",
                    ReturnTypeName = "System.Void",
                    MinimumVersion = "1.0.0",
                    MaximumVersion = "2.0.0",
                    IntegrationName = "AwsDynamoDb")]
                public static class AwsDynamoDbIntegration
                {
                }
            }

            [AttributeUsage(AttributeTargets.Class | AttributeTargets.Struct, AllowMultiple = true)]
            public class InstrumentMethodAttribute : Attribute
            {
                public string AssemblyName { get; set; }
                public string TypeName { get; set; }
                public string MethodName { get; set; }
                public string ReturnTypeName { get; set; }
                public string MinimumVersion { get; set; }
                public string MaximumVersion { get; set; }
                public string IntegrationName { get; set; }
            }
            """;

        var (diagnostics, outputs) = TestHelpers.GetGeneratedTrees<IntegrationNameToKeyGenerator>(
            [sourceCode],
            assertOutput: false);
        var output = outputs.FirstOrDefault() ?? string.Empty;

        using var s = new AssertionScope();
        diagnostics.Should().BeEmpty();

        // Verify the three key formats are generated correctly
        output.Should().Contain("DD_TRACE_AWSDYNAMODB_ENABLED", "uppercase canonical key");
        output.Should().Contain("DD_TRACE_AwsDynamoDb_ENABLED", "mixed case alias");
        output.Should().Contain("DD_AwsDynamoDb_ENABLED", "short form alias");
    }

    [Fact]
    public void GeneratesAlphabeticallySortedSwitch()
    {
        const string sourceCode = """
            using System;

            namespace Datadog.Trace.ClrProfiler.AutoInstrumentation.TestIntegration
            {
                [InstrumentMethod(
                    AssemblyName = "TestAssembly",
                    TypeName = "TestType",
                    MethodName = "TestMethod",
                    ReturnTypeName = "System.Void",
                    MinimumVersion = "1.0.0",
                    MaximumVersion = "2.0.0",
                    IntegrationName = "Zebra")]
                public static class ZebraIntegration
                {
                }

                [InstrumentMethod(
                    AssemblyName = "TestAssembly",
                    TypeName = "TestType2",
                    MethodName = "TestMethod2",
                    ReturnTypeName = "System.Void",
                    MinimumVersion = "1.0.0",
                    MaximumVersion = "2.0.0",
                    IntegrationName = "Apple")]
                public static class AppleIntegration
                {
                }

                [InstrumentMethod(
                    AssemblyName = "TestAssembly",
                    TypeName = "TestType3",
                    MethodName = "TestMethod3",
                    ReturnTypeName = "System.Void",
                    MinimumVersion = "1.0.0",
                    MaximumVersion = "2.0.0",
                    IntegrationName = "Mango")]
                public static class MangoIntegration
                {
                }
            }

            [AttributeUsage(AttributeTargets.Class | AttributeTargets.Struct, AllowMultiple = true)]
            public class InstrumentMethodAttribute : Attribute
            {
                public string AssemblyName { get; set; }
                public string TypeName { get; set; }
                public string MethodName { get; set; }
                public string ReturnTypeName { get; set; }
                public string MinimumVersion { get; set; }
                public string MaximumVersion { get; set; }
                public string IntegrationName { get; set; }
            }
            """;

        var (diagnostics, outputs) = TestHelpers.GetGeneratedTrees<IntegrationNameToKeyGenerator>(
            [sourceCode],
            assertOutput: false);
        var output = outputs.FirstOrDefault() ?? string.Empty;

        using var s = new AssertionScope();
        diagnostics.Should().BeEmpty();

        // Extract the switch cases and verify they're in alphabetical order
        var appleIndex = output.IndexOf("\"Apple\"");
        var mangoIndex = output.IndexOf("\"Mango\"");
        var zebraIndex = output.IndexOf("\"Zebra\"");

        appleIndex.Should().BeGreaterThan(0);
        mangoIndex.Should().BeGreaterThan(appleIndex, "Mango should come after Apple");
        zebraIndex.Should().BeGreaterThan(mangoIndex, "Zebra should come after Mango");
    }

    private static void AssertContains(string actual, string expected)
    {
        var normalizedActual = string.Join("\n", actual.Split('\n').Select(l => l.TrimEnd()));
        var normalizedExpected = string.Join("\n", expected.Split('\n').Select(l => l.TrimEnd()));
        normalizedActual.Should().Contain(normalizedExpected);
    }
}
