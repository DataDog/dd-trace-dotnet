        [Fact]
        public void CanGenerateTagsListWithTag()
        {
            const string input = @"using Datadog.Trace.SourceGenerators;
namespace MyTests.TestListNameSpace
{
    public class TestList 
    { 
        [Tag(""TestId"")]
    	public string Id { get; set; }
    }
}";
            const string expected = @"// <auto-generated/>
#nullable enable

namespace MyTests.TestListNameSpace
{
    partial class TestList
    {
        private static readonly byte[] IdBytes = Datadog.Trace.Vendors.MessagePack.StringEncoding.UTF8.GetBytes(""TestId"");

        public override string? GetTag(string key)
        {
            return key switch
            {
                ""TestId"" => Id,
                _ => base.GetTag(key),
            };
        }

        public override void SetTag(string key, string value)
        {
            switch(key)
            {
                case ""TestId"": 
                    Id = value;
                    break;
                default: 
                    base.SetTag(key, value);
                    break;
            }
        }

        protected override int WriteAdditionalTags(ref byte[] bytes, ref int offset)
        {
            var count = 0;
            if (Id != null)
            {
                count++;
                WriteTag(ref bytes, ref offset, IdBytes, Id);
            }

            return count + base.WriteAdditionalTags(ref bytes, ref offset);
        }

        protected override void WriteAdditionalTags(System.Text.StringBuilder sb)
        {
            if (Id != null)
            {
                sb.Append(""TestId (tag):"")
                  .Append(Id)
                  .Append(',');
            }

            base.WriteAdditionalTags(sb);
        }
    }
}
";

            var (diagnostics, output) = TestHelpers.GetGeneratedOutput<TagListGenerator>(input);
            Assert.Equal(expected, output);
            Assert.Empty(diagnostics);
        }

