using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.IO;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;

namespace GeneratePackageVersions
{
    public class XUnitFileGenerator : FileGenerator
    {
        private const string HeaderConst =
@"//------------------------------------------------------------------------------
// <auto-generated>
//     This code was generated by the GeneratePackageVersions tool. To safely
//     modify this file, edit PackageVersionsGeneratorDefinitions.json and
//     re-run the GeneratePackageVersions project in Visual Studio. See the
//     launchSettings.json for the project if you would like to run the tool
//     with the correct arguments outside of Visual Studio.

//     Changes to this file may cause incorrect behavior and will be lost if
//     the code is regenerated. 
// </auto-generated>
//------------------------------------------------------------------------------

using System.Collections.Generic;
using System.Diagnostics.CodeAnalysis;

namespace Datadog.Trace.ClrProfiler.IntegrationTests
{{
    [SuppressMessage(""StyleCop.CSharp.LayoutRules"", ""SA1516:Elements must be separated by blank line"", Justification = ""This is an auto-generated file."")]
    public class {0}
    {{";

        private const string FooterConst =
@"    }
}";

        private const string EntryFormat = @"
                new object[] {{ ""{0}"" }},";

        private const string BodyFormat =
@"      public static IEnumerable<object[]> {0} =>

            new List<object[]>
            {{
#if DEFAULT_SAMPLES
                new object[] {{ string.Empty }},
#else{1}
#endif
            }};
";

        private const string EndIfDirectiveConst =
@"
#endif";

        private readonly string _className;

        public XUnitFileGenerator(string filename, string className)
            : base(filename)
        {
            _className = className;
        }

        protected override string Header
        {
            get
            {
                return string.Format(HeaderConst, _className);
            }
        }

        protected override string Footer
        {
            get
            {
                return FooterConst;
            }
        }

        public override void Write(PackageVersionEntry packageVersionEntry, IEnumerable<(TargetFramework framework, IEnumerable<Version> versions)> versions, string requiresDockerDependency)
        {
            Debug.Assert(Started, "Cannot call Write() before calling Start()");
            Debug.Assert(!Finished, "Cannot call Write() after calling Finish()");

            var bodyStringBuilder = new StringBuilder();

            foreach (var (framework, packageVersions) in versions)
            {
                var fx = framework.ToString().ToUpper().Replace('.', '_');

                bodyStringBuilder.Append(@$"
#if {fx}");
                foreach (var version in packageVersions)
                {
                    bodyStringBuilder.AppendFormat(EntryFormat, version);
                }

                bodyStringBuilder.Append(EndIfDirectiveConst);
            }

            if (bodyStringBuilder.Length == 0)
            {
                // If we don't have _any_ versions, add the "default sample" so that we have _something_
                // Otherwise XUnit will error if it's called
                bodyStringBuilder.Append(@"
                new object[] { string.Empty },");
            }

            FileStringBuilder.AppendLine(string.Format(BodyFormat, packageVersionEntry.IntegrationName, bodyStringBuilder.ToString()));
        }

        /// <summary>
        /// Reads the previously-generated xunit file and extracts the version list per
        /// (integration, target framework). Used by Freeze mode to re-emit the same versions
        /// without re-querying NuGet -- feeding them back through Write() reproduces identical
        /// output because the writer is deterministic.
        /// Returns an empty dictionary if the file does not exist yet.
        /// </summary>
        public Dictionary<string, List<(TargetFramework Framework, IEnumerable<Version> Versions)>> LoadExistingVersions()
        {
            var result = new Dictionary<string, List<(TargetFramework, IEnumerable<Version>)>>();
            if (!File.Exists(Filename))
            {
                return result;
            }

            // The file is authored by Write() above. Each integration is emitted as:
            //   public static IEnumerable<object[]> <Integration> =>
            //       ...
            //       #if <FX>
            //           new object[] { "1.2.3" },
            //       #endif
            //       ...
            // A simple line-based scanner mirrors that structure directly.
            var integrationRegex = new Regex(@"public static IEnumerable<object\[\]> (\w+) =>");
            var ifRegex = new Regex(@"^\s*#if\s+(\w+)\s*$");
            var versionRegex = new Regex(@"new object\[\]\s*\{\s*""([^""]+)""\s*\}");

            List<(TargetFramework, IEnumerable<Version>)> currentIntegration = null;
            List<Version> currentVersions = null;

            foreach (var line in File.ReadAllLines(Filename))
            {
                var integrationMatch = integrationRegex.Match(line);
                if (integrationMatch.Success)
                {
                    currentIntegration = new List<(TargetFramework, IEnumerable<Version>)>();
                    result[integrationMatch.Groups[1].Value] = currentIntegration;
                    currentVersions = null;
                    continue;
                }

                if (currentIntegration is null)
                {
                    continue;
                }

                var ifMatch = ifRegex.Match(line);
                if (ifMatch.Success)
                {
                    var token = ifMatch.Groups[1].Value;
                    if (token == "DEFAULT_SAMPLES")
                    {
                        // The DEFAULT_SAMPLES branch contains only "new object[] { string.Empty }"
                        // which has no version literal for the regex to match; skip entirely.
                        currentVersions = null;
                        continue;
                    }

                    // Convert the preprocessor token (e.g. "NETCOREAPP3_1") back to the
                    // TargetFramework string Write() consumed (e.g. "netcoreapp3.1").
                    var frameworkText = token.ToLowerInvariant().Replace('_', '.');
                    var framework = (TargetFramework)new TargetFramework.TargetFrameworkTypeConverter().ConvertFrom(frameworkText);

                    currentVersions = new List<Version>();
                    currentIntegration.Add((framework, currentVersions));
                    continue;
                }

                if (currentVersions is null)
                {
                    continue;
                }

                var versionMatch = versionRegex.Match(line);
                if (versionMatch.Success)
                {
                    currentVersions.Add(new Version(versionMatch.Groups[1].Value));
                }
            }

            return result;
        }
    }
}
