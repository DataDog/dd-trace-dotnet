using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Text;
using System.Text.RegularExpressions;

#nullable enable

namespace GeneratePackageVersions;

/// <summary>
/// Generates MSBuild .g.props files containing PackageVersionSample items.
/// These are imported by Datadog.Trace.proj to build each sample project
/// at each version/TFM combination.
///
/// When some integrations are marked as <see cref="IntegrationVersions.Unchanged"/>,
/// the existing file is read and those integrations' items are preserved verbatim.
/// </summary>
public static class PropsFileGenerator
{
    // Matches each <PackageVersionSample>...</PackageVersionSample> block including leading indentation.
    private static readonly Regex ItemRegex = new(
        @"([ \t]*<PackageVersionSample\b[^>]*>.*?</PackageVersionSample>[ \t]*\r?\n)",
        RegexOptions.Singleline | RegexOptions.Compiled);

    private static readonly Regex SampleNameRegex = new(
        @"<SampleName>([^<]+)</SampleName>",
        RegexOptions.Compiled);

    private const string Header = """
        <!--
        ***********************************************************************************************
        PackageVersions.g.props

        WARNING: This file is auto-generated by the GeneratePackageVersions tool.
        Do not edit manually. Instead, modify IntegrationDefinitions.cs and re-run the tool.
        ***********************************************************************************************
        -->
        <Project>
          <ItemGroup>
        """;

    private const string Footer = """
          </ItemGroup>
        </Project>
        """;

    public static void Generate(string outputPath, GenerationResult result, Func<IntegrationVersions, List<FrameworkVersionGroup>> selector)
    {
        // If any integrations are unchanged, load existing items so we can preserve them.
        var existingBySample = new Dictionary<string, string>();
        var hasUnchanged = result.Integrations.Any(i => i.Unchanged);
        if (hasUnchanged && File.Exists(outputPath))
        {
            existingBySample = ParseExistingItems(File.ReadAllText(outputPath));
        }

        // Track which SampleNames we've already emitted (multiple integrations can share one).
        var emittedSamples = new HashSet<string>();

        var sb = new StringBuilder();
        sb.AppendLine(Header);

        foreach (var integration in result.Integrations)
        {
            if (integration.Unchanged)
            {
                // Preserve existing content, but only once per SampleName
                var sampleName = integration.Definition.SampleProjectName;
                if (emittedSamples.Add(sampleName)
                    && existingBySample.TryGetValue(sampleName, out var existing))
                {
                    sb.Append(existing);
                }

                continue;
            }

            var groups = selector(integration);
            var definition = integration.Definition;

            foreach (var group in groups)
            {
                foreach (var version in group.Versions)
                {
                    var skipAlpine = definition.ShouldSkipAlpine(version);
                    var skipArm64 = definition.ShouldSkipArm64(version);

                    sb.Append($"""
                            <PackageVersionSample Include="test\test-applications\{definition.TestFolder}\{definition.SampleProjectName}\{definition.SampleProjectName}.csproj">
                              <Properties>ApiVersion={version};RestoreRecursive=false;BuildProjectReferences=false</Properties>
                              <TargetFramework>{group.Framework}</TargetFramework>
                        """);

                    if (skipAlpine)
                    {
                        sb.AppendLine();
                        sb.Append("      <SkipAlpine>true</SkipAlpine>");
                    }

                    if (skipArm64)
                    {
                        sb.AppendLine();
                        sb.Append("      <SkipArm64>true</SkipArm64>");
                    }

                    sb.AppendLine();
                    sb.AppendLine($"      <RequiresDockerDependency>{definition.RequiresDockerDependency}</RequiresDockerDependency>");
                    sb.AppendLine($"      <SampleName>{definition.SampleProjectName}</SampleName>");
                    sb.AppendLine("    </PackageVersionSample>");
                }
            }
        }

        sb.Append(Footer);
        File.WriteAllText(outputPath, sb.ToString());
    }

    /// <summary>
    /// Parses an existing .g.props file and groups raw XML text by SampleName.
    /// Returns a dictionary: SampleName -> all PackageVersionSample elements for that sample as raw text.
    /// </summary>
    private static Dictionary<string, string> ParseExistingItems(string content)
    {
        var result = new Dictionary<string, string>();

        foreach (Match itemMatch in ItemRegex.Matches(content))
        {
            var itemText = itemMatch.Value;
            var sampleMatch = SampleNameRegex.Match(itemText);
            if (!sampleMatch.Success)
            {
                continue;
            }

            var sampleName = sampleMatch.Groups[1].Value;
            if (!result.ContainsKey(sampleName))
            {
                result[sampleName] = string.Empty;
            }

            result[sampleName] += itemText;
        }

        return result;
    }
}
