            };
        }

        internal static NativeCallTargetDefinition[] GetNativeDefinitions(InstrumentationDefinitions.Payload payload)
        {
            var defs = new List<NativeCallTargetDefinition>();
            for (var i = 0; i < payload.Definitions.Length; i++)
            {
                defs.Add(payload.Definitions[i].NativeCallTargetDefinition);
            }

            var definitionsArray = defs.ToArray();
            return definitionsArray;
        }

        internal struct Payload
        {
            public string DefinitionsId { get; set; }

            public InstrumentationDefinition[] Definitions { get; set; }
        }
    }
