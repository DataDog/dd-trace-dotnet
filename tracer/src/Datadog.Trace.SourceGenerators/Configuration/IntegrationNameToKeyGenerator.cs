using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;

/// <summary>
/// Source generator that scans InstrumentMethod attributes and generates
/// a switch case mapping integration names to their configuration keys.
/// </summary>
[Generator]
public class IntegrationNameToKeyGenerator : IIncrementalGenerator
{
    private const string AttributeName = "InstrumentMethodAttribute";
    private const string IntegrationNameProperty = "IntegrationName";

    /// <inheritdoc />
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // Find all attribute syntax nodes with IntegrationName assignments
        var attributesWithIntegrationName = context.SyntaxProvider
            .CreateSyntaxProvider(
                predicate: static (node, _) => node is AttributeSyntax,
                transform: static (ctx, _) => GetIntegrationNameFromAttribute(ctx))
            .Where(static name => !string.IsNullOrEmpty(name))
            .Collect();

        // Generate the output
        context.RegisterSourceOutput(attributesWithIntegrationName, Execute);
    }

    private static string? GetIntegrationNameFromAttribute(GeneratorSyntaxContext context)
    {
        var attributeSyntax = (AttributeSyntax)context.Node;

        // Check if this is an InstrumentMethod attribute
        var attributeSymbol = context.SemanticModel.GetSymbolInfo(attributeSyntax).Symbol;
        if (attributeSymbol is not IMethodSymbol methodSymbol ||
            methodSymbol.ContainingType?.Name != AttributeName)
        {
            return null;
        }

        // Look for IntegrationName = ... in the attribute arguments
        if (attributeSyntax.ArgumentList is null)
        {
            return null;
        }

        foreach (var argument in attributeSyntax.ArgumentList.Arguments)
        {
            // Check if this is a named argument for IntegrationName
            if (argument.NameEquals?.Name.Identifier.Text != IntegrationNameProperty)
            {
                continue;
            }

            // Try to get the constant value
            var constantValue = context.SemanticModel.GetConstantValue(argument.Expression);
            if (constantValue is { HasValue: true, Value: string integrationName })
            {
                return integrationName;
            }
        }

        return null;
    }

    private static void Execute(SourceProductionContext context, ImmutableArray<string?> integrationNameValues)
    {
        if (integrationNameValues.IsDefaultOrEmpty)
        {
            return;
        }

        // Extract unique integration names
        var integrationNames = new HashSet<string>(StringComparer.Ordinal);

        foreach (var name in integrationNameValues)
        {
            if (!string.IsNullOrEmpty(name))
            {
                integrationNames.Add(name!);
            }
        }

        if (integrationNames.Count == 0)
        {
            return;
        }

        // Generate the source code
        var source = GenerateSource(integrationNames.OrderBy(x => x, StringComparer.Ordinal).ToList());
        context.AddSource("IntegrationNameToKeys.g.cs", source);
    }

    private static string GenerateSource(List<string> integrationNames)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("namespace Datadog.Trace.Configuration");
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Generated mapping of integration names to their configuration keys.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    internal static partial class IntegrationNameToKeys");
        sb.AppendLine("    {");
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// Gets the configuration keys for the specified integration name.");
        sb.AppendLine("        /// Returns keys in order: [canonical key, alias1, alias2]");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        /// <param name=\"integrationName\">The integration name</param>");
        sb.AppendLine("        /// <returns>Array of configuration keys to check in order</returns>");
        sb.AppendLine("        public static string[] GetIntegrationEnabledKeys(string integrationName)");
        sb.AppendLine("        {");
        sb.AppendLine("            return integrationName switch");
        sb.AppendLine("            {");

        foreach (var name in integrationNames)
        {
            var upperKey = $"DD_TRACE_{name.ToUpperInvariant()}_ENABLED";
            var mixedKey = $"DD_TRACE_{name}_ENABLED";
            var shortKey = $"DD_{name}_ENABLED";

            sb.AppendLine($"                \"{name}\" => new[] {{ \"{upperKey}\", \"{mixedKey}\", \"{shortKey}\" }},");
        }

        sb.AppendLine("                _ => new[]");
        sb.AppendLine("                {");
        sb.AppendLine("                    string.Format(\"DD_TRACE_{0}_ENABLED\", integrationName.ToUpperInvariant()),");
        sb.AppendLine("                    string.Format(\"DD_TRACE_{0}_ENABLED\", integrationName),");
        sb.AppendLine("                    $\"DD_{integrationName}_ENABLED\"");
        sb.AppendLine("                }");
        sb.AppendLine("            };");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }
}
