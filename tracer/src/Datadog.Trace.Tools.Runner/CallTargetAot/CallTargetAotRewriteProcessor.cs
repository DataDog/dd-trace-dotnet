#nullable enable

using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Reflection;
using Datadog.Trace.ClrProfiler;
using Datadog.Trace.Tools.Runner.Aot;
using Datadog.Trace.Vendors.Newtonsoft.Json;
using Mono.Cecil;
using Mono.Cecil.Cil;

namespace Datadog.Trace.Tools.Runner.CallTargetAot;

/// <summary>
/// Rewrites the compiled app assembly and selected reference assemblies so their module initializers call the generated bootstrap.
/// </summary>
internal static class CallTargetAotRewriteProcessor
{
    /// <summary>
    /// Applies the rewrite to the compiled application assembly and selected references captured by the generated targets file.
    /// </summary>
    /// <param name="manifestPath">The manifest path generated by <c>calltarget-aot generate</c>.</param>
    /// <param name="applicationAssemblyPath">The compiled application assembly path that should be rewritten.</param>
    /// <param name="referenceAssemblyList">The semicolon-delimited reference assembly list captured by the generated targets file.</param>
    /// <param name="outputDirectory">The rewrite output directory.</param>
    /// <returns>Zero when the rewrite succeeds; otherwise one.</returns>
    internal static int Process(string manifestPath, string applicationAssemblyPath, string? referenceAssemblyList, string outputDirectory)
    {
        if (!File.Exists(manifestPath))
        {
            throw new FileNotFoundException("The CallTarget AOT manifest does not exist.", manifestPath);
        }

        if (!File.Exists(applicationAssemblyPath))
        {
            throw new FileNotFoundException("The application assembly does not exist.", applicationAssemblyPath);
        }

        Directory.CreateDirectory(outputDirectory);
        var manifest = JsonConvert.DeserializeObject<CallTargetAotManifest>(File.ReadAllText(manifestPath))
                       ?? throw new InvalidOperationException("The CallTarget AOT manifest could not be deserialized.");

        var resolver = CreateResolver(applicationAssemblyPath, referenceAssemblyList, manifest.RegistryAssemblyPath);
        var readerParameters = new ReaderParameters
        {
            AssemblyResolver = resolver,
            InMemory = true,
            ReadSymbols = false
        };

        using var registryAssembly = AssemblyDefinition.ReadAssembly(manifest.RegistryAssemblyPath, new ReaderParameters { ReadSymbols = false });
        RewriteSingleAssembly(applicationAssemblyPath, outputDirectory, manifest, registryAssembly.MainModule, readerParameters);

        foreach (var referenceAssemblyPath in EnumerateReferenceAssemblyPaths(referenceAssemblyList))
        {
            RewriteSingleAssembly(referenceAssemblyPath, outputDirectory, manifest, registryAssembly.MainModule, readerParameters);
        }

        return 0;
    }

    /// <summary>
    /// Rewrites or copies a single application or reference assembly into the rewrite output directory.
    /// </summary>
    /// <param name="assemblyPath">The source assembly path.</param>
    /// <param name="outputDirectory">The rewrite output directory.</param>
    /// <param name="manifest">The manifest that describes the selected bindings.</param>
    /// <param name="registryModule">The generated registry module.</param>
    /// <param name="readerParameters">The reader parameters configured for the rewrite session.</param>
    private static void RewriteSingleAssembly(
        string assemblyPath,
        string outputDirectory,
        CallTargetAotManifest manifest,
        ModuleDefinition registryModule,
        ReaderParameters readerParameters)
    {
        var assemblyName = Path.GetFileNameWithoutExtension(assemblyPath);
        var shouldRewrite = manifest.RewritePlan.TargetAssemblyNames.Count == 0 ||
                            manifest.RewritePlan.TargetAssemblyNames.Contains(assemblyName, StringComparer.OrdinalIgnoreCase);
        var outputAssemblyPath = Path.Combine(outputDirectory, Path.GetFileName(assemblyPath));

        if (!shouldRewrite)
        {
            File.Copy(assemblyPath, outputAssemblyPath, overwrite: true);
            return;
        }

        AssemblyDefinition assembly;
        try
        {
            assembly = AssemblyDefinition.ReadAssembly(assemblyPath, readerParameters);
        }
        catch (BadImageFormatException ex)
        {
            throw new BadImageFormatException($"Failed to read rewritten CallTarget assembly candidate '{assemblyPath}'.", ex);
        }

        using (assembly)
        {
            var definitionItems = BuildDefinitionItems(assembly, manifest);
            if (definitionItems.Count == 0)
            {
                File.Copy(assemblyPath, outputAssemblyPath, overwrite: true);
                return;
            }

            _ = AotProcessor.ProcessDefinitions(assembly.MainModule, definitionItems);
            InjectBootstrapCall(assembly.MainModule, registryModule, manifest);
            assembly.Write(outputAssemblyPath);
        }
    }

    /// <summary>
    /// Builds the legacy rewriter definition items used to patch the compiled assembly with CallTarget invocations.
    /// </summary>
    /// <param name="applicationAssembly">The compiled application assembly being rewritten.</param>
    /// <param name="manifest">The manifest that describes the matched CallTarget definitions.</param>
    /// <returns>The definition items that should be rewritten in the application assembly.</returns>
    private static List<DefinitionItem> BuildDefinitionItems(AssemblyDefinition applicationAssembly, CallTargetAotManifest manifest)
    {
        var tracerAssembly = Assembly.LoadFrom(manifest.TracerAssemblyPath);
        var tracerAssemblyName = tracerAssembly.GetName().Name ?? "Datadog.Trace";
        var applicationAssemblyName = applicationAssembly.Name.Name;
        var definitions = new List<DefinitionItem>();

        foreach (var match in manifest.MatchedDefinitions.Where(match => string.Equals(match.TargetAssemblyName, applicationAssemblyName, StringComparison.Ordinal)))
        {
            var integrationType = tracerAssembly.GetType(match.IntegrationTypeName, throwOnError: false);
            if (integrationType is null)
            {
                continue;
            }

            var targetType = ResolveType(applicationAssembly.MainModule, match.TargetTypeName);
            if (targetType is null)
            {
                continue;
            }

            var targetMethod = targetType.Methods.FirstOrDefault(method =>
                string.Equals(method.Name, match.TargetMethodName, StringComparison.Ordinal) &&
                string.Equals(method.ReturnType.FullName, match.ReturnTypeName, StringComparison.Ordinal) &&
                method.Parameters.Count == match.ParameterTypeNames.Count &&
                method.Parameters.Select(static parameter => parameter.ParameterType.FullName).SequenceEqual(match.ParameterTypeNames, StringComparer.Ordinal));
            if (targetMethod is null)
            {
                continue;
            }

            var signatureTypes = new[] { match.ReturnTypeName }.Concat(match.ParameterTypeNames).ToArray();
            var definition = new NativeCallTargetDefinition(
                applicationAssemblyName,
                match.TargetTypeName,
                match.TargetMethodName,
                signatureTypes,
                0,
                0,
                0,
                ushort.MaxValue,
                ushort.MaxValue,
                ushort.MaxValue,
                tracerAssemblyName,
                match.IntegrationTypeName);
            definitions.Add(new DefinitionItem(applicationAssembly, targetType, targetMethod, integrationType, definition));
        }

        return definitions;
    }

    /// <summary>
    /// Creates an assembly resolver that can resolve the app assembly, its local references, and the generated registry assembly.
    /// </summary>
    /// <param name="applicationAssemblyPath">The application assembly being rewritten.</param>
    /// <param name="referenceAssemblyList">The semicolon-delimited list of copy-local references emitted by MSBuild.</param>
    /// <param name="registryAssemblyPath">The generated registry assembly path.</param>
    /// <returns>The resolver configured for the rewrite operation.</returns>
    private static DefaultAssemblyResolver CreateResolver(string applicationAssemblyPath, string? referenceAssemblyList, string registryAssemblyPath)
    {
        var resolver = new DefaultAssemblyResolver();
        var searchDirectories = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
        {
            Path.GetDirectoryName(Path.GetFullPath(applicationAssemblyPath)) ?? Directory.GetCurrentDirectory(),
            Path.GetDirectoryName(Path.GetFullPath(registryAssemblyPath)) ?? Directory.GetCurrentDirectory()
        };

        var normalizedReferenceAssemblyList = referenceAssemblyList ?? string.Empty;
        if (!string.IsNullOrWhiteSpace(normalizedReferenceAssemblyList))
        {
            foreach (var referencePath in normalizedReferenceAssemblyList.Split(new[] { ';' }, StringSplitOptions.RemoveEmptyEntries))
            {
                var directory = Path.GetDirectoryName(referencePath);
                if (!string.IsNullOrWhiteSpace(directory))
                {
                    _ = searchDirectories.Add(directory!);
                }
            }
        }

        foreach (var directory in searchDirectories)
        {
            resolver.AddSearchDirectory(directory);
        }

        return resolver;
    }

    /// <summary>
    /// Enumerates the unique reference assembly paths captured by the generated MSBuild target.
    /// </summary>
    /// <param name="referenceAssemblyList">The semicolon-delimited list of copy-local references emitted by MSBuild.</param>
    /// <returns>The normalized reference assembly paths.</returns>
    private static IEnumerable<string> EnumerateReferenceAssemblyPaths(string? referenceAssemblyList)
    {
        if (string.IsNullOrWhiteSpace(referenceAssemblyList))
        {
            yield break;
        }

        var normalizedReferenceAssemblyList = referenceAssemblyList!;
        var seenPaths = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        foreach (var referenceAssemblyPath in normalizedReferenceAssemblyList.Split(new[] { ';' }, StringSplitOptions.RemoveEmptyEntries))
        {
            if (string.IsNullOrWhiteSpace(referenceAssemblyPath) || !File.Exists(referenceAssemblyPath))
            {
                continue;
            }

            var extension = Path.GetExtension(referenceAssemblyPath);
            if (!string.Equals(extension, ".dll", StringComparison.OrdinalIgnoreCase) &&
                !string.Equals(extension, ".exe", StringComparison.OrdinalIgnoreCase))
            {
                continue;
            }

            var fullPath = Path.GetFullPath(referenceAssemblyPath);
            if (seenPaths.Add(fullPath))
            {
                yield return fullPath;
            }
        }
    }

    /// <summary>
    /// Resolves a type by full name from the supplied module or its exported type forwards.
    /// </summary>
    /// <param name="module">The module to search.</param>
    /// <param name="typeFullName">The fully qualified type name.</param>
    /// <returns>The resolved type definition, if found.</returns>
    private static TypeDefinition? ResolveType(ModuleDefinition module, string typeFullName)
    {
        var typeDefinition = module.Types.FirstOrDefault(type => string.Equals(type.FullName, typeFullName, StringComparison.Ordinal));
        if (typeDefinition is not null)
        {
            return typeDefinition;
        }

        return module.ExportedTypes.FirstOrDefault(type => string.Equals(type.FullName, typeFullName, StringComparison.Ordinal))?.Resolve();
    }

    /// <summary>
    /// Injects the milestone bootstrap call into the module initializer of the rewritten assembly.
    /// </summary>
    /// <param name="applicationModule">The application module being rewritten.</param>
    /// <param name="registryModule">The generated registry module that defines the milestone bootstrap.</param>
    /// <param name="manifest">The manifest that names the bootstrap type and method.</param>
    private static void InjectBootstrapCall(ModuleDefinition applicationModule, ModuleDefinition registryModule, CallTargetAotManifest manifest)
    {
        var registryAssemblyName = AssemblyNameReference.Parse(registryModule.Assembly.FullName);
        if (!applicationModule.AssemblyReferences.Any(reference => string.Equals(reference.FullName, registryAssemblyName.FullName, StringComparison.Ordinal)))
        {
            applicationModule.AssemblyReferences.Add(registryAssemblyName);
        }

        var bootstrapType = registryModule.Types.Single(type =>
            string.Equals(type.FullName, manifest.RegistryBootstrapTypeName, StringComparison.Ordinal));
        var bootstrapMethod = bootstrapType.Methods.Single(method =>
            string.Equals(method.Name, manifest.RegistryBootstrapMethodName, StringComparison.Ordinal));
        var importedBootstrapMethod = applicationModule.ImportReference(bootstrapMethod);
        var consoleWriteLineMethod = typeof(Console).GetMethod(nameof(Console.WriteLine), new[] { typeof(string) })
                                     ?? throw new InvalidOperationException("System.Console.WriteLine(string) could not be resolved.");
        var importedConsoleWriteLineMethod = applicationModule.ImportReference(consoleWriteLineMethod);
        var moduleType = applicationModule.Types.Single(type => type.Name == "<Module>");
        var moduleInitializer = moduleType.Methods.FirstOrDefault(method => method.Name == ".cctor");

        if (moduleInitializer is null)
        {
            moduleInitializer = new MethodDefinition(
                ".cctor",
                Mono.Cecil.MethodAttributes.Private | Mono.Cecil.MethodAttributes.Static | Mono.Cecil.MethodAttributes.HideBySig | Mono.Cecil.MethodAttributes.SpecialName | Mono.Cecil.MethodAttributes.RTSpecialName,
                applicationModule.TypeSystem.Void)
            {
                IsRuntimeSpecialName = true
            };
            moduleType.Methods.Add(moduleInitializer);
            var processor = moduleInitializer.Body.GetILProcessor();
            AppendBootstrapSequence(processor, importedBootstrapMethod, importedConsoleWriteLineMethod, manifest.BootstrapMarker, appendRet: true);
            return;
        }

        var existingBody = moduleInitializer.Body;
        var firstInstruction = existingBody.Instructions.FirstOrDefault();
        var il = existingBody.GetILProcessor();
        InsertBootstrapSequence(il, firstInstruction, importedBootstrapMethod, importedConsoleWriteLineMethod, manifest.BootstrapMarker);
    }

    /// <summary>
    /// Appends the bootstrap call sequence to a newly created module initializer body.
    /// </summary>
    /// <param name="processor">The IL processor that owns the initializer body.</param>
    /// <param name="bootstrapMethod">The imported bootstrap method reference.</param>
    /// <param name="consoleWriteLineMethod">The imported console write-line method reference.</param>
    /// <param name="bootstrapMarker">The marker string written when the bootstrap reports first initialization.</param>
    /// <param name="appendRet">Whether to append a trailing return instruction.</param>
    private static void AppendBootstrapSequence(ILProcessor processor, MethodReference bootstrapMethod, MethodReference consoleWriteLineMethod, string bootstrapMarker, bool appendRet)
    {
        var skipMarkerInstruction = Instruction.Create(OpCodes.Nop);
        processor.Append(Instruction.Create(OpCodes.Call, bootstrapMethod));
        processor.Append(Instruction.Create(OpCodes.Brfalse_S, skipMarkerInstruction));
        processor.Append(Instruction.Create(OpCodes.Ldstr, bootstrapMarker));
        processor.Append(Instruction.Create(OpCodes.Call, consoleWriteLineMethod));
        processor.Append(skipMarkerInstruction);
        if (appendRet)
        {
            processor.Append(Instruction.Create(OpCodes.Ret));
        }
    }

    /// <summary>
    /// Inserts the bootstrap call sequence at the start of an existing module initializer body.
    /// </summary>
    /// <param name="processor">The IL processor that owns the initializer body.</param>
    /// <param name="firstInstruction">The first existing instruction in the initializer body.</param>
    /// <param name="bootstrapMethod">The imported bootstrap method reference.</param>
    /// <param name="consoleWriteLineMethod">The imported console write-line method reference.</param>
    /// <param name="bootstrapMarker">The marker string written when the bootstrap reports first initialization.</param>
    private static void InsertBootstrapSequence(ILProcessor processor, Instruction? firstInstruction, MethodReference bootstrapMethod, MethodReference consoleWriteLineMethod, string bootstrapMarker)
    {
        if (firstInstruction is null)
        {
            AppendBootstrapSequence(processor, bootstrapMethod, consoleWriteLineMethod, bootstrapMarker, appendRet: true);
            return;
        }

        var callInstruction = Instruction.Create(OpCodes.Call, bootstrapMethod);
        var branchInstruction = Instruction.Create(OpCodes.Brfalse_S, firstInstruction);
        var loadMarkerInstruction = Instruction.Create(OpCodes.Ldstr, bootstrapMarker);
        var writeMarkerInstruction = Instruction.Create(OpCodes.Call, consoleWriteLineMethod);

        processor.InsertBefore(firstInstruction, callInstruction);
        processor.InsertAfter(callInstruction, branchInstruction);
        processor.InsertAfter(branchInstruction, loadMarkerInstruction);
        processor.InsertAfter(loadMarkerInstruction, writeMarkerInstruction);
    }
}
