using Microsoft.CodeAnalysis;

namespace Datadog.Trace.Tools.Analyzers.EnumToStringAnalyzer;

/// <summary>
/// Diagnostic descriptors for the EnumToString analyzer.
/// </summary>
public static class Diagnostics
{
    /// <summary>
    /// The diagnostic ID displayed in error messages.
    /// </summary>
    public const string DiagnosticId = "DDALLOC005";

#pragma warning disable RS2008 // Enable analyzer release tracking for the analyzer project
    internal static readonly DiagnosticDescriptor EnumToStringRule = new(
        DiagnosticId,
        title: "Enum.ToString() allocates",
        messageFormat: "Calling ToString() on enum '{0}' boxes the value and allocates via reflection; use a ToStringFast() extension (via [EnumExtensions] source generator), a switch expression, or a cached lookup instead",
        category: "Performance",
        defaultSeverity: DiagnosticSeverity.Info,
        isEnabledByDefault: true,
        description: "Enum.ToString() boxes the value and allocates a string via reflection on all runtimes. Use a ToStringFast() extension method (generated by [EnumExtensions]), a switch expression, or a cached dictionary lookup to avoid the allocation.");
#pragma warning restore RS2008
}
